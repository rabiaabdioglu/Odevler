	LIST	P=16F84
	INCLUDE	"P16F84.INC"
	ORG	0X00
SAYAC1	EQU	H'0C'
SAYAC2	EQU	H'0D'
	BSF	STATUS,5
	CLRF	PORTB
	BSF	TRISA,0
	BCF	STATUS,5
	CLRF	PORTB
PROGRAM:

TEST_PORTA:
	MCLR
	BTFSS	PORTA,0
	GOTO	SAG	
	NOP

	BTFSS	PORTA,1
	GOTO	SOL
	NOP
	
	BTFSS	MCLR,1
	GOTO	PROGRAM

	GOTO PROGRAM		;ALGORITMA TEKRARLANIR

SOL
	CALL	GECIKME
	RLF	PORTB,F
	BTFSS	PORTB,7
	GOTO	SOL
SAG
	CALL	GECIKME
	RRF	PORTB,F
	BTFSS	PORTB,0
	GOTO	SAG

GECIKME
	MOVLW	D'255'
	MOVWF	SAYAC1
YUKLE1
	MOVLW	D'255'
	MOVWF	SAYAC2
AZALT
	DECFSZ	SAYAC2,F
	GOTO	AZALT	
	DECFSZ	SAYAC1,F
	GOTO	YUKLE1
	RETURN
	END
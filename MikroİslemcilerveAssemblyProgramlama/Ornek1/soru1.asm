	LIST	P=16F84
	INCLUDE	"P16F84.INC"
	
	CLRF	PORTB			; PORTB VE PORTA SIFIRLANIR
	CLRF	PORTA
	BSF	STATUS,5
	CLRF	TRISB
	CLRF	PORTB
	MOVLW	B'00000000'
	MOVWF	TRISB
	BCF	STATUS,5
	

PROGRAM

	BTFSC	PORTA,0
	GOTO	BIRLER
	BTFSC	PORTA,1
	GOTO	ONLAR


ONLAR
	BCF	PORTB
	MOVLW	h'4F'
	MOVWF	PORTB
	ADDWF	PCL,F
	RETLW	0X4F
	GOTO	PROGRAM
BIRLER
	BCF	PORTB
	MOVLW	h'7F'
	MOVWF	PORTB
	ADDWF	PCL,F
	RETLW	0X7F
	GOTO	PROGRAM

	END

